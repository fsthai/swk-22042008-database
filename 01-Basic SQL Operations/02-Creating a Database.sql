# SQL แสดงรายชื่อฐานข้อมูลทั้งหมดในระบบฐานข้อมูลที่เรามี
show databases;


# SQL สร้างฐานข้อมูลใหม่
# CREATE DATABASE <ชื่อฐานข้อมูลทีต้องการสร้าง>
CREATE DATABASE dog;
CREATE DATABASE school_store;
CREATE DATABASE users;


# SQL ลบฐานข้อมูลออกจากระบบ
# DROP DATABASE <name>;
DROP DATABASE dog;
DROP DATABASE school_store;

# SQL เรียกใช้งานฐานข้อมูล ..ต้องเรียกทุกครั้งก่อนดำเนินการอะไรก็ตามกับฐานข้อมูลนั้น
# USE <database name>;
USE dog;


